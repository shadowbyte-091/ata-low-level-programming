 #include <stdio.h>

// This program demonstrates variables in C

int main(void)
{
    int age = 20;          // integer variable
    float height = 5.9;    // float variable
    char grade = 'A';      // character variable

    printf("Age: %d, Height: %.1f, Grade: %c\n", age, height, grade);

    return 0;
}

