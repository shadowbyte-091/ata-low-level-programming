#include <stdio.h>

// This program demonstrates the use of C keywords

int main(void)    // int and main are keywords
{
    const int limit = 1;   // const and int are keywords
    for (int i = 0; i < limit; i++)   // for and int are keywords
    {
        if (i == 0)   // if is a keyword
        {
            printf("Keywords are powerful\n");
        }
    }
    return 0;   // return is a keyword
}

